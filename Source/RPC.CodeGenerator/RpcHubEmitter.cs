    static void EmitPartialClass(StringBuilder sb, RpcHubEmitModel model, string indent)
    {
        string hub = model.HubTypeName;

        sb.AppendLine($"{indent}public partial class {hub}");
        sb.AppendLine($"{indent}{{");

        EmitConstructors(sb, model, indent + "    ");

        if (model.IsServerEndpoint)
        {
            EmitListenAsync(sb, hub, indent + "    ");
        }
        else
        {
            EmitConnectAsync(sb, hub, indent + "    ");
        }

        foreach (var proc in model.Outgoing)
        {
            EmitOutgoingProcedure(sb, proc, indent + "    ");
        }

        foreach (var proc in model.Incoming)
        {
            EmitIncomingProcedure(sb, proc, indent + "    ");
        }

        EmitDefaultMessageConverter(sb, indent + "    ");

        if (model.NeedsStringHelpers)
        {
            EmitStringHelpers(sb, indent + "    ");
        }

        sb.AppendLine($"{indent}}}");
    }

    static void EmitConstructors(StringBuilder sb, RpcHubEmitModel model, string indent)
