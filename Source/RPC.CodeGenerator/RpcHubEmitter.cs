    static void EmitOutgoingProcedure(StringBuilder sb, MethodMetadata proc, string indent)
    {
        string syncReturn = proc.ReturnType.SpecialType == SpecialType.System_Void
            ? "void"
            : proc.ReturnType.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat);

        string asyncReturn = proc.ReturnType.SpecialType == SpecialType.System_Void
            ? "global::System.Threading.Tasks.Task"
            : $"global::System.Threading.Tasks.Task<{proc.ReturnType.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)}>";

        var paramList = string.Join(", ", proc.Parameters.Select(p =>
            $"{p.Type.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)} {p.Name}"));

        // Sync
        if (proc.ReturnType.SpecialType == SpecialType.System_Void)
        {
            sb.AppendLine($"{indent}public void {proc.MethodName}({paramList})");
            sb.AppendLine($"{indent}{{");
            if (proc.Parameters.Length == 0)
            {
                sb.AppendLine($"{indent}    byte[] parameterData = global::System.Array.Empty<byte>();");
            }
            else
            {
                RpcBinaryCodec.EmitSerializeParameters(sb, indent + "    ", proc.Parameters);
            }

            sb.AppendLine($"{indent}    global::System.Threading.Tasks.Task<byte[]> rpcTask = RequestRPC({proc.MethodId}, parameterData, {proc.ReliableTypeExpression});");
            sb.AppendLine($"{indent}    rpcTask.GetAwaiter().GetResult();");
            sb.AppendLine($"{indent}}}");
        }
        else
        {
            sb.AppendLine($"{indent}public {syncReturn} {proc.MethodName}({paramList})");
            sb.AppendLine($"{indent}{{");
            if (proc.Parameters.Length == 0)
            {
                sb.AppendLine($"{indent}    byte[] parameterData = global::System.Array.Empty<byte>();");
            }
            else
            {
                RpcBinaryCodec.EmitSerializeParameters(sb, indent + "    ", proc.Parameters);
            }

            sb.AppendLine($"{indent}    global::System.Threading.Tasks.Task<byte[]> rpcTask = RequestRPC({proc.MethodId}, parameterData, {proc.ReliableTypeExpression});");
            sb.AppendLine($"{indent}    byte[] resultBytes = rpcTask.GetAwaiter().GetResult();");
            sb.AppendLine($"{indent}    return {RpcMarshal.EmitDecodeReturnExpression(proc.ReturnType, "resultBytes")};");
            sb.AppendLine($"{indent}}}");
        }

        sb.AppendLine();

        // Async
        if (proc.ReturnType.SpecialType == SpecialType.System_Void)
        {
            sb.AppendLine($"{indent}public async {asyncReturn} {proc.MethodName}Async({paramList})");
            sb.AppendLine($"{indent}{{");
            if (proc.Parameters.Length == 0)
            {
                sb.AppendLine($"{indent}    byte[] parameterData = global::System.Array.Empty<byte>();");
            }
            else
            {
                RpcBinaryCodec.EmitSerializeParameters(sb, indent + "    ", proc.Parameters);
            }

            sb.AppendLine($"{indent}    await RequestRPC({proc.MethodId}, parameterData, {proc.ReliableTypeExpression});");
            sb.AppendLine($"{indent}}}");
        }
        else
        {
            sb.AppendLine($"{indent}public async {asyncReturn} {proc.MethodName}Async({paramList})");
            sb.AppendLine($"{indent}{{");
            if (proc.Parameters.Length == 0)
            {
                sb.AppendLine($"{indent}    byte[] parameterData = global::System.Array.Empty<byte>();");
            }
            else
            {
                RpcBinaryCodec.EmitSerializeParameters(sb, indent + "    ", proc.Parameters);
            }

            sb.AppendLine($"{indent}    byte[] resultBytes = await RequestRPC({proc.MethodId}, parameterData, {proc.ReliableTypeExpression});");
            sb.AppendLine($"{indent}    return {RpcMarshal.EmitDecodeReturnExpression(proc.ReturnType, "resultBytes")};");
            sb.AppendLine($"{indent}}}");
        }

        sb.AppendLine();
    }

    static void EmitIncomingProcedure(StringBuilder sb, MethodMetadata proc, string indent)
    {
        sb.AppendLine($"{indent}private byte[] {proc.MethodName}_Requested(byte[] parameterData)");
        sb.AppendLine($"{indent}{{");

        if (proc.Parameters.Length == 0)
        {
            if (proc.ReturnType.SpecialType == SpecialType.System_Void)
            {
                sb.AppendLine($"{indent}    {proc.MethodName}_Implementation();");
                sb.AppendLine($"{indent}    return global::System.Array.Empty<byte>();");
            }
            else
            {
                sb.AppendLine($"{indent}    {proc.ReturnType.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)} result = {proc.MethodName}_Implementation();");
                sb.AppendLine($"{indent}    return {RpcMarshal.EmitEncodeReturnExpression(proc.ReturnType, "result")};");
            }
        }
        else
        {
            sb.AppendLine($"{indent}    using (var __rpcMs = new global::System.IO.MemoryStream(parameterData))");
            sb.AppendLine($"{indent}    using (var __rpcR = new global::System.IO.BinaryReader(__rpcMs))");
            sb.AppendLine($"{indent}    {{");
            RpcBinaryCodec.EmitDeserializeLocals(sb, indent + "        ", proc.Parameters, "__rpcR");
            string args = RpcBinaryCodec.BuildArgumentListFromLocals(proc.Parameters);
            if (proc.ReturnType.SpecialType == SpecialType.System_Void)
            {
                sb.AppendLine($"{indent}        {proc.MethodName}_Implementation({args});");
                sb.AppendLine($"{indent}    }}");
                sb.AppendLine($"{indent}    return global::System.Array.Empty<byte>();");
            }
            else
            {
                sb.AppendLine(
                    $"{indent}        {proc.ReturnType.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)} result = {proc.MethodName}_Implementation({args});");
                sb.AppendLine($"{indent}        return {RpcMarshal.EmitEncodeReturnExpression(proc.ReturnType, "result")};");
                sb.AppendLine($"{indent}    }}");
            }
        }

        sb.AppendLine($"{indent}}}");
        sb.AppendLine();

        string partialKeyword = proc.ReturnType.SpecialType == SpecialType.System_Void ? "partial void" : $"partial {proc.ReturnType.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)}";
        sb.AppendLine($"{indent}private {partialKeyword} {proc.MethodName}_Implementation({ParameterDeclarationList(proc)});");
        sb.AppendLine();
    }

    static string ParameterDeclarationList(MethodMetadata proc)
    {
        return string.Join(", ", proc.Parameters.Select(p =>
            $"{p.Type.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat)} {p.Name}"));
    }

    static void EmitDefaultMessageConverter(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}private sealed class DefaultMessageConverter : Communication.Shared.Messages.IMessageConverter");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    public byte[] Serialize(object message)");
        sb.AppendLine($"{indent}    {{");
        sb.AppendLine($"{indent}        return MessageSerializer.Serialize(message);");
        sb.AppendLine($"{indent}    }}");
        sb.AppendLine();
        sb.AppendLine($"{indent}    public object Deserialize(global::System.ReadOnlySpan<byte> messageData)");
        sb.AppendLine($"{indent}    {{");
        sb.AppendLine($"{indent}        return MessageSerializer.Deserialize(messageData.ToArray());");
        sb.AppendLine($"{indent}    }}");
        sb.AppendLine($"{indent}}}");
        sb.AppendLine();
    }

    static void EmitStringHelpers(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}private static byte[] RpcEncodeString(string value)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    value ??= string.Empty;");
        sb.AppendLine($"{indent}    var bytes = global::System.Text.Encoding.UTF8.GetBytes(value);");
        sb.AppendLine($"{indent}    var buf = new byte[4 + bytes.Length];");
        sb.AppendLine($"{indent}    global::System.Buffer.BlockCopy(global::System.BitConverter.GetBytes(bytes.Length), 0, buf, 0, 4);");
        sb.AppendLine($"{indent}    global::System.Buffer.BlockCopy(bytes, 0, buf, 4, bytes.Length);");
        sb.AppendLine($"{indent}    return buf;");
        sb.AppendLine($"{indent}}}");
        sb.AppendLine();
        sb.AppendLine($"{indent}private static string RpcDecodeString(byte[] data)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    if (data == null || data.Length < 4) return string.Empty;");
        sb.AppendLine($"{indent}    int len = global::System.BitConverter.ToInt32(data, 0);");
        sb.AppendLine($"{indent}    if (len <= 0 || 4 + len > data.Length) return string.Empty;");
        sb.AppendLine($"{indent}    return global::System.Text.Encoding.UTF8.GetString(data, 4, len);");
        sb.AppendLine($"{indent}}}");
        sb.AppendLine();
    }
}

